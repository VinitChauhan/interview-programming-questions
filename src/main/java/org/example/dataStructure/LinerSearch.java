package org.example.dataStructure;

public class LinerSearch {
    public static void main(String[] args) {
        int n = 10;
        for ( int i = 0 ; i < n ; i++ ) {
            System.out.print("Counter : " + i);
        }
//        Time Complexity --> 1+n+n+n-->1+3n-->3n-->O(n)-->Linear time
    }
}
